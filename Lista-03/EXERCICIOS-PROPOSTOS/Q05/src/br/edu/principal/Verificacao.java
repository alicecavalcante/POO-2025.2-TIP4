package br.edu.principal;

public class Verificacao {
	    public static String ParImpar(int n) {
	       if (n<0){
	return "O número " + n + " é negativo.";

	} 
	else if(n>0){ 
	return "O número " + n + " é positivo.";
	}
	else { 
	return "O número é zero (neutro). ";
	}
	    }
}


